import sys
import asyncio
from typing import List, Dict, Any

try:
    from deepeval.metrics import AnswerRelevancyMetric, FaithfulnessMetric
    from deepeval.test_case import LLMTestCase
    from deepeval import evaluate
except ImportError:
    print("deepeval not installed. Please run: pip install deepeval", file=sys.stderr)

try:
    from ragas import evaluate as ragas_evaluate
    from ragas.metrics import faithfulness, answer_relevancy, context_precision, context_recall
    from datasets import Dataset
except ImportError:
    print("ragas or datasets not installed. Please run: pip install ragas datasets", file=sys.stderr)

from backend.logging_config import get_logger
logger = get_logger(__name__)

async def run_deepeval_evals(test_cases_data: List[Dict[str, str]]):
    """
    Run DeepEval evaluations.
    """
    logger.info("Running DeepEval evaluations...")
    test_cases = []
    for data in test_cases_data:
        test_case = LLMTestCase(
            input=data["input"],
            actual_output=data["actual_output"],
            expected_output=data["expected_output"],
            retrieval_context=[data["context"]]
        )
        test_cases.append(test_case)
    
    answer_relevancy_metric = AnswerRelevancyMetric(threshold=0.7)
    faithfulness_metric = FaithfulnessMetric(threshold=0.7)
    
    # Run evaluation
    results = evaluate(
        test_cases,
        [answer_relevancy_metric, faithfulness_metric],
        print_results=False
    )
    logger.info(f"DeepEval results: {results}")
    return results

def run_ragas_evals(test_cases_data: List[Dict[str, str]]):
    """
    Run Ragas evaluations.
    """
    logger.info("Running Ragas evaluations...")
    
    # Ragas expects a HuggingFace Dataset
    data_dict = {
        "question": [d["input"] for d in test_cases_data],
        "answer": [d["actual_output"] for d in test_cases_data],
        "contexts": [[d["context"]] for d in test_cases_data],
        "ground_truth": [d["expected_output"] for d in test_cases_data]
    }
    dataset = Dataset.from_dict(data_dict)
    
    result = ragas_evaluate(
        dataset,
        metrics=[faithfulness, answer_relevancy, context_precision, context_recall]
    )
    logger.info(f"Ragas results: {result}")
    
    # JD-48: Block deployment if retrieval quality drops
    # Check context_precision and context_recall thresholds (>= 0.70)
    for metric_name in ["context_precision", "context_recall"]:
        score = result.get(metric_name)
        if score is not None and score < 0.70:
            logger.error(f"Ragas metric {metric_name} failed: {score} < 0.70")
            raise ValueError(f"Retrieval quality drop: {metric_name} score is {score}")
            
    return result

def compare_field_level_outputs(expected: Dict[str, Any], actual: Dict[str, Any]) -> Dict[str, Any]:
    """
    Perform field-level output comparison for agent evaluation.
    """
    logger.info("Running field-level output comparison...")
    results = {}
    all_keys = set(expected.keys()).union(set(actual.keys()))
    
    for key in all_keys:
        expected_val = expected.get(key)
        actual_val = actual.get(key)
        match = expected_val == actual_val
        results[key] = {
            "expected": expected_val,
            "actual": actual_val,
            "match": match
        }
        if match:
            logger.debug(f"Field '{key}' matches.")
        else:
            logger.warning(f"Field '{key}' mismatch: expected '{expected_val}', got '{actual_val}'.")
            
    return results

async def main():
    logger.info("Starting evaluation framework...")
    
    # Dummy data for demonstration. In a real scenario, this would load 
    # the outputs generated by the AI agents and ground truth from a dataset.
    dummy_data = [
        {
            "input": "What is the requirement for the backend engineer?",
            "actual_output": "The requirement is Python and FastAPI.",
            "expected_output": "Python and FastAPI experience is required.",
            "context": "Job description: We need a backend engineer with Python and FastAPI."
        }
    ]
    
    # Dummy structured data for field-level comparison
    expected_structure = {"title": "Backend Engineer", "skills": ["Python", "FastAPI"], "years": 3}
    actual_structure = {"title": "Backend Engineer", "skills": ["Python", "FastAPI"], "years": 2}
    
    try:
        if 'deepeval' in sys.modules:
            await run_deepeval_evals(dummy_data)
        if 'ragas' in sys.modules:
            run_ragas_evals(dummy_data)
        
        # Run field-level comparison
        field_level_results = compare_field_level_outputs(expected_structure, actual_structure)
        logger.info(f"Field-level comparison results: {field_level_results}")
        
        logger.info("Evaluations completed successfully.")
        sys.exit(0)
    except Exception as e:
        logger.error(f"Evaluation failed: {e}", exc_info=True)
        sys.exit(1)

if __name__ == "__main__":
    asyncio.run(main())
